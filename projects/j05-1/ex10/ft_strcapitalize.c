char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] > 64) && (str[i] < 91))
			str[i] += 32;
		i++;
	}
	i = 0;
	if ((str[i] > 96) && (str[i] < 123))
		str[i] -= 32;
	while (str[i++] != '\0')
	{
		if (str[i - 1] == ' ' || ((str[i - 1] > 32) && (str[i - 1] < 65)) ||
				((str[i - 1] > 90) && (str[i - 1] < 97)))
		{
			if (((str[i] > 96) && (str[i] < 123)) &&
					!((str[i - 1] > 47) && str[i - 1] < 58))
				str[i] -= 32;
		}
	}
	return (str);
}
